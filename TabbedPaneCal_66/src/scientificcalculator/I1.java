   public double npr(long n,long r)
    {
        return (double)fact(n)/(double)fact(n-r);
      
    }
    public double ncr(long n,long r)
    {
        return (double)fact(n)/((double)fact(r)*(double)fact(n-r));
        
    }
    
    public long fact(long x)
    {
        long fact = 1;
        for(long i=1;i<=x;i++)
        {
            fact*=i;
        }
        return fact;
    }
