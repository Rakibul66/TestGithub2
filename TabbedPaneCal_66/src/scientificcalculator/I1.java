    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        // TODO add your handling code here:
        chk = 1;
        int i = (tf1.getText().length())-len;
        
        try {
            b = Double.parseDouble(tf1.getText(len,i));
        } catch (BadLocationException ex) {
            Logger.getLogger(I1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       switch(iden)
       {
           case 1:
           {
               tf2.setText(Double.toString(a+b));
               iden = 0;
               p = 0;
               break;
           }
           case 2:
           {
               tf2.setText(Double.toString(a-b));
               iden = 0;
               p = 0;
               break;
           }
           case 3:
           {
               tf2.setText(Double.toString(a*b));
               iden = 0;
               p = 0;
               break;
           }
           case 4:
           {
               tf2.setText(Double.toString(a/b)); 
               iden = 0;
               p = 0;
               break;
           }
           case 5:
           {
               tf2.setText(Double.toString(Math.pow(a, b))); 
               iden = 0;
               p = 0;
               break;
           }
           case 6:
           {
            if(b<=a)   tf2.setText(Double.toString(npr((int)a,(int)b)));  
            else tf2.setText("Error -> r>n");
            iden = 0;
               p = 0;
               break;               
           }
           case 7:
           {
               if(b<=a)tf2.setText(Double.toString(ncr((int)a,(int)b))); 
               else tf2.setText("Error -> r>n");
               
               iden = 0;
               p = 0;
               break;              
           }
           default:
           {
               tf2.setText(tf1.getText());
               iden = 0;
               p = 0;
               break;
           }
       }
    }//GEN-LAST:event_jButton19ActionPerformed

